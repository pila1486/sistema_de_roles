    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String usuario = txtUsuario.getText();
        String contrasena = txtPass.getText();
        User usuarioCorrecto = controladora.validarUsuario(usuario, contrasena);
        
        if(usuarioCorrecto != null ){           
            String rol = usuarioCorrecto.getUnRol().getNombreRol();
             if(rol.equals("admin")){
                 PrincipalAdmin pAdmin = new PrincipalAdmin (controladora, usuarioCorrecto);
                 pAdmin.setVisible(true);
                 pAdmin.setLocationRelativeTo(null);
                 this.dispose();
              }
              if(rol.equals("user")){
                  PrincipalUser pUser = new PrincipalUser(controladora, usuarioCorrecto);
                  pUser.setVisible(true);
                  pUser.setLocationRelativeTo(null);
                  this.dispose();
              }
        
        }else{
            txtMensaje.setText("Usuario o contraseña incorrectos");
        }
        
        
        
    }//GEN-LAST:event_btnLoginActionPerformed


